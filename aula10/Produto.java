class Produto {
    public int id;
    public String nome;
    public double preco;
    public int estoque;

    public Produto() {}

    public Produto(int id, String nome, double preco, int estoque) {
        this.id = id;
        this.nome = nome;
        this.preco = preco;
        this.estoque = estoque;
    }
}